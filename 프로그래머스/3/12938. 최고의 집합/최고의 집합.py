# n=4, s=9 : [2,2,2,3] => 9//4=2, 9%4=1
# n=5, s=9 : [1,2,2,2,2] => 
# n=5, s=9 : 1+1+1+1+5 / 1+1+1+2+4 / 1+1+1+3+3 / 1+1+2+2+3 / 1+2+2+2+2
        #    9 = 5 * 1 + 4 
        #    9//5=1, 9%5=4 => 1을 5개 뿌리고, 4개ㅇ는 1씩 더 더해줌
def solution(n, s):
    if s//n == 0:
        answer = [-1]
    else:
        answer = [s//n] * n
        for i in range((n-s%n),n):
            answer[i] += 1

    return answer